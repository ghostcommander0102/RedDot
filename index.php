		precision mediump float;
		uniform vec2 u_mouse;
		uniform vec2 u_res;
		varying vec2 v_texcoord;
	
		void main() {
			vec2 position = v_texcoord;
			
			vec3 lastcolor = vec3(0.1 ,0.1, 0.1);
	
			vec3 o = vec3(0);
			o += lastcolor * 0.5;
			
